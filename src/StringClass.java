
public class StringClass {

	public static void main(String[] args) {
		String s =new String (".Java");
		System.out.println("String=.java  SLength= "+s.length());
		
		int slength=s.length();
		System.out.println(slength);
		int maxIndex=slength-1;
		System.out.println("Maximum Index of string= "+maxIndex);
		
		System.out.println("____________________________");
		String s2=new String("Vijaya@123");
		System.out.println("String=Vijaya@123  sLength= "+s2.length());
		
		String s3=new String(" 1 2 3 ");
		System.out.println("String= 1 2 3  sLength= "+s3.length());
		
		

	}

}
