package loopingStatement;

public class WhileLoop 
{
	public static void main(String[] args)
	{
		int a=10;
		while(a<=100)
		{
			System.out.println(a);
			a=a+10;
		}
		
		//While DEcrement Loop
		System.out.println("--------");
		int b=50;
		while(b>=5)
		{
			System.out.println(b);
			b=b-5;
		}
		
	}
}
