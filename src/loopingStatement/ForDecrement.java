package loopingStatement;

public class ForDecrement 
{
public static void main(String[] args)
{
	for(int x=20;x>=2;x=x-2)
	{
		System.out.println(x);
	}
	
	System.out.println("---------");
	
	for(int x=100;x>=10;x=x-10)
	{
		System.out.println(x);
	}
	
	System.out.println("-----");
	
	for(int a=1;a<=5;a++)
	{
		System.out.println("sangli");//String repetation
	}
}
}
